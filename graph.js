const loopAnimation = (ele, i) => {
  const offset = {
    style: {
      "line-dash-offset": -10 * i,
    },
  };
  const duration = { duration: 100 };
  return ele
    .animation(offset, duration)
    .play()
    .promise("complete")
    .then(() => loopAnimation(ele, i + 1));
};

const stopAnimation = (ele) => {
  ele.stop();
};

cy.on("mouseover", "node", function (event) {
  let connectedEdges = event.target.connectedEdges();
  if (event.target._private.data.bio) {
    popUpEl.style.display = "block";
    popUpEl.innerHTML = event.target._private.data.bio;
  }
  connectedEdges.toggleClass("active");
  connectedEdges.targets().addClass("active");
  connectedEdges.sources().addClass("active");
  connectedEdges.forEach((edge) => {
    loopAnimation(edge, 1);
  });
});

cy.on("mouseout", "node", function (event) {
  let connectedEdges = event.target.connectedEdges();
  popUpEl.style.display = "none";
  connectedEdges.toggleClass("active");
  connectedEdges.targets().removeClass("active");
  connectedEdges.sources().removeClass("active");
  connectedEdges.forEach((edge) => {
    stopAnimation(edge);
  });
});

